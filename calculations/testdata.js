function calculateMean(dataset){
    // console.log('calculateMean dataset:', dataset)
    let sum = 0;
    for(let i = 0; i < dataset.length; i++){
        sum += dataset[i];
    }
    return sum / dataset.length;
}
function calculateMedian(dataset){
    // console.log('calculateMedian dataset:', dataset)
    dataset.sort();
    let mid = Math.floor(dataset.length / 2);
    if(dataset.length % 2 === 0){
        return (dataset[mid - 1] + dataset[mid]) / 2;
    }
    return dataset[mid];
}
function calculateMode(dataset){
    // console.log('calculateMode dataset:', dataset)
    let mode = 0;
    let max = 0;
    let count = 0;
    for(let i = 0; i < dataset.length; i++){
        for(let j = 0; j < dataset.length; j++){
            if(dataset[i] === dataset[j]){
                count++;
            }
        }
        if(count > max){
            max = count;
            mode = dataset[i];
        }
        count = 0;
    }
    return mode;
}
function calculateRange(dataset){
    let min = dataset[0];
    let max = dataset[0];
    for(let i = 0; i < dataset.length; i++){
        if(dataset[i] < min){
            min = dataset[i];
        }
        if(dataset[i] > max){
            max = dataset[i];
        }
    }
    return max - min;
}
function calculateStandardDeviation(dataset){
    let mean = calculateMean(dataset);
    let sum = 0;
    for(let i = 0; i < dataset.length; i++){
        sum += Math.pow(dataset[i] - mean, 2);
    }
    return Math.sqrt(sum / dataset.length);
}
